public class Attributes {

    protected int STR; //Strength
    protected int FOR; //Fortitude
    protected int VIT; //Vitality
    protected int LUC; //Luck

    private void upSTR() {
        STR++;
    }

    private void upFOR() {
        FOR++;
    }

    private void upVIT() {
        VIT++;
    }

    private void upLUC() {
        LUC++;
    }

}
